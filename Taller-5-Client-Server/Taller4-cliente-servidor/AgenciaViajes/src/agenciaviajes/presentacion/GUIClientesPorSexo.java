    @Override
    public void actualizar(AModel amodel){
        try{
            GestorClientes gestor = (GestorClientes) amodel;
            gestor.consultarClientes();
            this.actualizarDatos(gestor);
            repaint();
                           
        }catch(Exception e){
            
        }
    }
    
    public void actualizarDatos(GestorClientes gestor){
        try{
        this.TotalHombres = gestor.numeroHombres();
        this.TotalMujeres = gestor.numeroMujeres();
        this.TotalClientes = this.TotalHombres+this.TotalMujeres;
        
        this.PorcentajeHombres = this.TotalHombres*100/this.TotalClientes;
        this.PorcentajeMujeres = this.TotalMujeres*100/this.TotalClientes;
    
        }catch(Exception e){
            
        }
    }
    
    public int mayorValor(int pHombres,int pMujeres){
        if(pHombres>pMujeres){
            return pHombres;
        }else{
            return pMujeres;
        }
    }
    
    public void paint(Graphics g){
        super.paint(g);
        int valorMayor = mayorValor(this.TotalHombres,this.TotalMujeres);
        int largoHombres = this.TotalHombres*300/this.TotalClientes;
        int largoMujeres = this.TotalMujeres*300/this.TotalClientes;
        
        g.setColor(new Color(255,0,0));
        g.fillRect(100, 100, largoHombres, 40);
        g.setColor(Color.BLACK);
        g.drawString("Hombres", 10, 125);
        g.drawString(Float.toString(this.PorcentajeHombres), 410, 130);
        g.drawRect(100,100, 300, 40);
        
        g.setColor(new Color(255,0,0));
        g.fillRect(100, 150, largoMujeres, 40);
        g.setColor(Color.BLACK);
        g.drawString("Mujeres", 10, 175);
        g.drawString(Float.toString(this.PorcentajeMujeres), 410, 170);
        g.drawRect(100,150, 300, 40);
        
        
    }
    
    
    
    
    
    
